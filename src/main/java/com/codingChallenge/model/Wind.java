package com.codingChallenge.model;

public class Wind {

    private Float speed;
    private Integer deg;

    public void Wind(){

    }

    public Float getSpeed(){
        return this.speed;
    }

    public void setSpeed(Float speed){
        this.speed = speed;
    }

    public Integer getDeg(){
        return this.deg;
    }

    public void setDeg(Integer deg){
        this.deg = deg;
    }
}