package com.codingChallenge.model;

public class Location {
    
    private Float longe ;
    private Float lat ;


    public void Location(){

    }

    public Float getLonge(){
        return this.longe;
    }

    public void setLonge ( Float longe ){
        this.longe = longe;
    }

    public Float getLat(){
        return this.lat;
    }

    public void setLat ( Float lat ){
        this.lat = lat;
    }
}

